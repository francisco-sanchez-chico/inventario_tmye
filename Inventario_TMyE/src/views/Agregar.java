    private int convertirTamanio(String tamanio) {
        switch (tamanio) {
            case "Chico":
                return 1;

            case "Mediano":
                return 2;

            case "Grande":
                return 3;

            case "Muy grande":
                return 4;
        }
        return -1;
    }

    private int convertirAlmacen(String almacen) {
        switch (almacen) {
            case "ALFA":
                return 1;

            case "BETA":
                return 2;
        }
        return -1;
    }

    private int convertirTipoProducto(String tipo) {
        switch (tipo) {
            case "Mueble":
                return 1;

            case "Electrodomestico":
                return 2;
        }
        return -1;
    }

