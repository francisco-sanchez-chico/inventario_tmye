    private void btn_agregar_productoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregar_productoMouseClicked
        Producto p = new Producto();
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        java.sql.Date fechaSql;
        java.util.Date fechaFila;
        try {
            if (this.textField_nombre_producto.getText().isBlank() || this.textField_nombre_producto.getText().isBlank()
                    || this.textField_precio_venta.getText().isBlank() || this.textField_precio_compra.getText().isBlank()
                    || this.textField_fecha_llegada.getText().isBlank() || this.textField_stock_comprado.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "No puede dejar los campos con * en blanco.");
            } else {
                String tamanio = this.cbox_tamanio.getSelectedItem().toString(),
                        tipo = this.combox_tipo_producto.getSelectedItem().toString(),
                        almacen = this.cbox_num_almacen.getSelectedItem().toString();

                p.setNombre(this.textField_nombre_producto.getText());
                p.setId_tamanio(convertirTamanio(tamanio));
                p.setId_almacen(convertirAlmacen(almacen));
                p.setTipo_producto(convertirTipoProducto(tipo));

                try {
                    p.setPrecio_venta(Float.parseFloat(this.textField_precio_venta.getText()));
                    p.setCosto_empresa(Float.parseFloat(this.textField_precio_compra.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Ingrese solamente números en los campos de Precio de venta y Precio de compra");
                }

                try {
                    fechaFila = formato.parse(this.textField_fecha_llegada.getText());
                    fechaSql = new java.sql.Date(fechaFila.getTime());
                    p.setFecha_llegada(fechaSql);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Para la Fecha que llegó ingrese la fecha con el siguiente formato: aaaa-mm-dd");
                }

                p.setCantidad_inventario(Integer.parseInt(this.textField_stock_comprado.getText()));
                if (ProductoDAO.Agregar(p)) {
                    JOptionPane.showMessageDialog(null, "Se ha agregado el producto!");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.toString());
        }
    }//GEN-LAST:event_btn_agregar_productoMouseClicked

    private void btn_agregar_ventaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregar_ventaMouseClicked
        Venta v = new Venta();
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        java.sql.Date fechaSql;
        java.util.Date fechaFila;
        try {
            if (this.textField_id_cliente.getText().isBlank() || this.textField_fecha_venta.getText().isBlank() || this.textField_monto_total.getText().isBlank()
                    || this.textField_primer_pago.getText().isBlank() || this.textField_pago_credito.getText().isBlank()
                    || this.textField_idProducto.getText().isBlank() || this.textField_cantidad.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "No puede dejar los campos con * en blanco.");
            } else {
                try{
                    v.setId_cliente(Integer.parseInt(this.textField_id_cliente.getText()));
                    v.setTotal(Float.parseFloat(this.textField_monto_total.getText()));
                    v.setPago_inicial(Float.parseFloat(this.textField_primer_pago.getText()));
                    v.setPago_credito(Float.parseFloat(this.textField_pago_credito.getText()));
                    v.setId_producto(Integer.parseInt(this.textField_idProducto.getText()));
                    v.setCantidad(Integer.parseInt(this.textField_cantidad.getText()));
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Todos los campos excepto Fecha de venta tienen que ser números");
                }
                
                try {
                    fechaFila = formato.parse(this.textField_fecha_venta.getText());
                    fechaSql = new java.sql.Date(fechaFila.getTime());
                    v.setFecha(fechaSql);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Para la Fecha de venta ingrese la fecha con el siguiente formato: aaaa-mm-dd");
                }
                if (VentaDAO.Agregar(v)) {
                    JOptionPane.showMessageDialog(null, "Se ha agregado la venta!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.toString());
        }
    }//GEN-LAST:event_btn_agregar_ventaMouseClicked

    private void btn_agregar_clienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregar_clienteMouseClicked
        Cliente c = new Cliente();
        try {
            if (this.textField_nombre_cliente.getText().isBlank() || this.textField_primer_apellido.getText().isBlank()
                    || this.textField_segundo_apellido.getText().isBlank() || this.textField_deuda_pendiente.getText().isBlank()
                    || this.textField_creditoDisponible.getText().isBlank() || this.textField_calle.getText().isBlank()
                    || this.textField_num_casa.getText().isBlank() || this.textField_colonia.getText().isBlank()) {
                JOptionPane.showMessageDialog(null, "No puede dejar los campos con * en blanco.");
            } else {
                c.setNombre((this.textField_nombre_cliente.getText()));
                c.setApellido1(this.textField_primer_apellido.getText());
                c.setApellido2(this.textField_segundo_apellido.getText());
                
                try{
                    c.setDeuda_pendiente(Float.parseFloat(this.textField_deuda_pendiente.getText()));
                    c.setCredito_disponible(Float.parseFloat(this.textField_creditoDisponible.getText()));
                }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(null, "Para los campos Deuda pendiente y Crédito disponible solo aceptan números mayor o igual a 0");
                    c.setDeuda_pendiente(-1);
                    c.setCredito_disponible(-1);
                }
                
                c.setCalle(this.textField_calle.getText());
                c.setNumero_casa(this.textField_num_casa.getText());
                c.setColonia(this.textField_colonia.getText());
                c.setTelefono(this.textField_telefono.getText());
                if(c.getDeuda_pendiente() < 0 || c.getCredito_disponible() < 0) {
                    JOptionPane.showMessageDialog(null, "Para los campos Deuda pendiente y Crédito disponible solo aceptan números mayor o igual a 0");
                }else{
                    if (ClienteDAO.Agregar(c)) {
                        JOptionPane.showMessageDialog(null, "Se ha agregado el cliente!");
                    }
                }
                System.out.println(Float.isNaN(c.getDeuda_pendiente()));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: "+ e.getMessage());
        }
    }//GEN-LAST:event_btn_agregar_clienteMouseClicked

