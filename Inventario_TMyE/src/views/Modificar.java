    private void btn_actualizarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarProductoActionPerformed
        try {
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date fecha = formato.parse(this.textField_productoFechaModificar.getText());
            java.sql.Date fechaSql = new java.sql.Date(fecha.getTime());

            Producto p = new Producto();
            p.setId_producto(Integer.parseInt(this.textField_productoIdProductoModificar.getText()));
            p.setId_almacen(convertirAlmacen(this.combo_productoAlmacenModificar.getSelectedItem().toString()));
            p.setNombre(this.textField_productoNombreModificar.getText());
            p.setId_tamanio(convertirTama単o(this.combo_productoTama単oModificar.getSelectedItem().toString()));
            p.setPrecio_venta(Float.parseFloat(this.textField_productoPrecioVentaModificar.getText()));
            p.setCosto_empresa(Float.parseFloat(this.textField_productoCostoEmpresaModificar.getText()));
            p.setFecha_llegada(fechaSql);
            p.setCantidad_inventario(Integer.parseInt(this.textField_productoCantidadModificar.getText()));
            p.setTipo_producto(convertirTipoProducto(this.combo_productoTipoProductoModificar.getSelectedItem().toString()));
            
            ProductoDAO.Modificar(p);
            mostrarContenido(this.cbox_tablas.getSelectedItem().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.toString());
        }
    }//GEN-LAST:event_btn_actualizarProductoActionPerformed

